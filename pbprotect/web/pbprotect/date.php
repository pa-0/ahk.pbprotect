// =====================================================================
// Check if a date logfile should be created.
// =====================================================================
if ($use_date_log == 1) {


  // ===================================================================
  // Open the date logfile.
  // ===================================================================
  $file_handle = fopen($log_file_dir."/date.log", "a");


  // ===================================================================
  // Check if the date logfile could be opened.
  // ===================================================================
  if ($file_handle) {


    // =================================================================
    // Get the remote ip address.
    // =================================================================
    $remote_ip = get_remote_ip();


    // =================================================================
    // Create the log string.
    // =================================================================
    $log_string = date('d.m.Y')." | ".date('H:i:s')." | ".$remote_ip."\n";


    // =================================================================
    // Write the string to the date logfile.
    // =================================================================
    fwrite($file_handle, $log_string);


    // =================================================================
    // Close the logfile.
    // =================================================================
    fclose($file_handle);

  } // Check if the date logfile could be opened.
} // Check if an date logfile should be created.


